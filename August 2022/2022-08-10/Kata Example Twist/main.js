/*

Codewars, 8k

This is an easy twist to the example kata (provided by Codewars when learning how to create your own kata).

Add the value "codewars" to the array websites/Websites 1,000 times.

*/
let websites = [];
let counter = 1;
while (counter <= 1000) {
  websites.push('codewars');
  counter++;
}